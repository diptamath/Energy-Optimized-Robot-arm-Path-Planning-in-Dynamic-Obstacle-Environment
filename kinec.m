function K=kinec(t,tr)

l1=0.73;
       l2=0.35;
       l3=0.15;
       
       
        t(1)=(t(1)*22)/(180*7);
       t(2)=(t(2)*22)/(180*7);
       t(3)=(t(3)*22)/(180*7);
       
       tr(1)=(tr(1)*22)/(180*7);
       tr(2)=(tr(2)*22)/(180*7);
       tr(3)=(tr(3)*22)/(180*7);
       
       t(4)=t(1)-tr(1);
       t(5)=t(2)-tr(2);
       t(6)=t(3)-tr(3);
       
        m1=5.4;
       m2=4.2;
       m3=3;
       
       I1=(m1+m2)*0.33*(l2*sin(t(2))+l3*sin(t(3)+t(2)))*(l2*sin(t(2))+l3*sin(t(3)+t(2)));
       I2=0.33*m2*l2*l2+0.33*m3*l3*l3+m3*l2*l2;
       I3=0.33*m3*l3*l3;
       
         
      
      
       
       K= I1*0.5*(t(4)*t(4))+I2*0.5*(t(5)*t(5))+I3*0.5*(t(6)*t(6));
end
       