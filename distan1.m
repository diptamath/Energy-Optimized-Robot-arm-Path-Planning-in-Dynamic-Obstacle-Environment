function d=distan1(t,x1,y1,z1)


       l1=0.73;
       l2=0.35;
       l3=0.15;
       
        t(1)=(t(1)*22)/(180*7);
       t(2)=(t(2)*22)/(180*7);
       t(3)=(t(3)*22)/(180*7);
     

  z=l2*cos(t(2))+l3*cos(t(3)+t(2))+l1;
       y=cos(t(1))*(l2*sin(t(2))+l3*sin(t(3)+t(2)));
       x=sin(t(1))*(l2*sin(t(2))+l3*sin(t(3)+t(2)));
       
       d=((x1-x)*(x1-x))+((y1-y)*(y1-y))+((z1-z)*(z1-z));
end